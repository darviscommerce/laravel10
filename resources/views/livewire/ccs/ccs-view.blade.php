                <h2>Winning</h2>
                <p>Bauxiet, de erstvorm van aluminium, bestaat voor het grootste deel uit Al203 (aluminiumoxide).
                    Zo&#x27;n 8,5% van het aardoppervlakte is bestreken met bauxiet. Hiermee is aluminium op zuurstof en
                    silicium na het meest aanwezige element op aarde.<br><br>Het bauxiet wordt in contact gebracht met
                    warme natronloog. De natronloog reageert met de aanwezige aluminiumoxide in het bauxiet. <em>Dit
                        vormt natrium hydroxide.</em> De overige componenten van het bauxiet bezinken en worden
                    gefilterd zodat er een zuivere hydroxide overblijft.<br><br>De natrium aluminium hydroxide wordt
                    vervolgens afgekoeld waardoor de aluminiumhydroxide kristalliseert en de natronloog overblijf. De
                    aluminiumhydroxide wordt verhit (gecalcineerd) en zo ontstaat aluminiumoxide, ook wel aluinaarde
                    genoemd.</p>
                <h2>Productie</h2>
                <p>Het aluminiumoxidepoeder (aluinaarde) wordt samen met kryoliet verhit. Bij een temperatuur van 1000
                    graden Celsius wordt er gelijkstroom door de vloeistof gevoerd. De aluinaarde is ondertussen
                    opgelost in het kryoliet en wordt door middel van elektrolyse weer gesplitst waarbij er vloeibaar
                    aluminium en zuurstof vrijkomt. Het vloeibare aluminium wordt <strong>daarna weggevoerd en naar de
                        gieterij getransporteerd</strong>. Daar wordt het gereinigd en eventueel vermengd met andere
                    materialen (afhankelijk van de kwaliteit die het aluminium moet krijgen). Uit het gemengde aluminium
                    worden walsplakken en persstaven gemaakt. Deze persstaven en walsplakken zijn de basis van de meeste
                    aluminium producten.<br><br>Van 4000 kilogram bauxiet kan zo&#x27;n 1000 kilogram aluminium gemaakt
                    worden.</p>
